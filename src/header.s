;; iNES HEADER FOR GENERAL INFORMATION ABOUT THE CARTRIDGE (16 Byte)
;; NROM-128 with 16 KB PRG ROM and 8 KB CHR ROM
.segment "INES"
        .byte $4E, $45, $53, $1A    ; 0-3 ("NES^Z")
        .byte $1                    ; 4 (num of 16 KB PRG ROM banks)
        .byte $1                    ; 5 (num of  8 KB CHR ROM banks)
        .byte $1                    ; 6 (flags) (mirroring: 0: horizontal, 1: vertical)
        .byte $1                    ; 7 (flags)
        .byte $1                    ; 8 (num of 8 KB PRG RAM banks)
        .byte $0                    ; 9 (flags) (TV SYSTEM: 0: NTSC, 1: PAL)
        .byte $0                    ; 10 (must be zero)
        .byte $0                    ; 11 (must be zero)
        .byte $0                    ; 12 (must be zero)
        .byte $0                    ; 13 (must be zero)
        .byte $0                    ; 14 (must be zero)
        .byte $0                    ; 15 (must be zero)